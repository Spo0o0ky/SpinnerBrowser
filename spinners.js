function getEntity(path, suffix, drawOutline) {
    // note: _id
    return `{
        {
            _fromLayer = "entities",
            _name = "FrostHelper/IceSpinner",
            _type = "entity",
            attachGroup = -1,
            attachToSolid = false,
            bloomAlpha = 0,
            bloomRadius = 0,
            borderColor = "000000",
            collidable = true,
            dashThrough = false,
            debrisCount = 8,
            destroyColor = "b0eaff",
            directory = "${path}",
            drawOutline = ${drawOutline},
            moveWithWind = false,
            rainbow = false,
            singleFGImage = false,
            spritePathSuffix = "${suffix}",
            tint = "ffffff",
            x = 320,
            y = 120
        }
    }`
}

function buttonClick(button) {
    info = spinners[button.id.substring(7)]; // 7 is length of text "button_"
    navigator.clipboard.writeText(getEntity(info[1], info[2], info[3]));
    button.innerHTML = "Copied!";
    button.style.backgroundColor = "#e1bde0"
    setDefaultColor(button)
}

async function setDefaultColor(button) {
    await sleep(1000);
    button.innerHTML = "Copy";
    button.style.backgroundColor = null;
}

function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

for (let i = 0; i < spinners.length; i++) {
    info = spinners[i];
    //div for that spinner
    let e = document.createElement("DIV");

    //linebreak
    let br = document.createElement("BR");

    //title
    let title = document.createElement("H2");
    title.innerHTML = info[0];

    //preview
    let image = document.createElement("IMG");
    image.setAttribute("src","./Images/spinners/" + info[0] + ".png");

    //"copy" button
    let button = document.createElement("BUTTON");
    button.innerHTML = "Copy";
    button.id = "button_" + i;
    button.setAttribute("onclick","buttonClick(this)");
        
    e.append(title);
    e.append(image);
    e.append(br);
    e.append(button);
    document.body.append(e);
}